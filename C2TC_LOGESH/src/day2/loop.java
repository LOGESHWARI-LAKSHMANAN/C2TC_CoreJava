package day2;

public class loop {
	public static void main(String args[]) {
		//for loop
		for (int i = 0; i <10; i++) {
			  System.out.println(i);
			}
		//while loop
		int i = 10;
		while (i < 20) {
		  System.out.println(i);
		  i++;
		}
		//do-while loop
		int j=0;
		do {
		  System.out.println(i);
		  j++;
		}
		while (j<3);

}
}
